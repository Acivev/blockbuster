package mchorse.blockbuster.api.formats.obj;

/**
 * Substitute class for a 3d vector which comes with joml library
 */
public class Vector3f
{
    public float x;
    public float y;
    public float z;

    public Vector3f(float x, float y, float z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
